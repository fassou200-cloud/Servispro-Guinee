// Helper functions to get data
export const getRegions = () => GUINEA_LOCATIONS.regions.map(r => ({ id: r.id, name: r.name }));

export const getVillesByRegion = (regionId) => {
  const region = GUINEA_LOCATIONS.regions.find(r => r.id === regionId);
  return region ? region.villes.map(v => ({ id: v.id, name: v.name })) : [];
};

export const getCommunesByVille = (regionId, villeId) => {
  const region = GUINEA_LOCATIONS.regions.find(r => r.id === regionId);
  if (!region) return [];
  const ville = region.villes.find(v => v.id === villeId);
  return ville ? ville.communes.map(c => ({ id: c.id, name: c.name })) : [];
};

export const getQuartiersByCommune = (regionId, villeId, communeId) => {
  const region = GUINEA_LOCATIONS.regions.find(r => r.id === regionId);
  if (!region) return [];
  const ville = region.villes.find(v => v.id === villeId);
  if (!ville) return [];
  const commune = ville.communes.find(c => c.id === communeId);
  return commune ? commune.quartiers : [];
};

export default GUINEA_LOCATIONS;
